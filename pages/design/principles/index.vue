        The global political climate suggests that we live at a crucial
        crossroads in time to choose whether past models of human interaction
        and production are sufficient to meet the needs of the future of our
        species and the future of our planet or we need new models.
      </p>
      <p>
        The evolutionary survival and success of our species threatens the
        biodiversity of our planet and is resulting in the rapid extinction of
        vast numbers of species.
      </p>
      <p>
        The tendency of the system we have designed toward homogeneity is
        destroying the delicate balance of the earth’s diverse ecosystem.
      </p>
      <p>
        Biology provides the model for a sustainable future. The symbiotic
        relationships of the various organisms that form our own bodies are a
        model of systems that have been proven to survive and thrive on earth.
      </p>
      <p>
        Individual cells are part of an interconnected body, a complex design
        that requires every part to function well for the health and well-being
        of the whole.
      </p>
      <p>
        The interdisciplinary nature of a biological organism has been proposed
        as a metaphor for understanding the ecosystem of the planet, involving
        both physical and biological processes. (See the Gaia hypothesis.)
      </p>
      <p>
        Physics and biology are the basis for a new movement in engineering and
        design called <a href="https://en.m.wikipedia.org/wiki/Biomimetics"
        target="_blank">biomimicry</a>. By modelling architecture after biology,
        we can create living systems that can live in harmony with each other
        and the natural environment, in a symbiotic relationship with the earth
        rather than parasitic.
      </p>
      <p>
        Diversity and multidisciplinary work are the best models we have for our
        survival and for the continued flourishing of the human species.
      </p>
