        If we have come to understand anything about the nature of creativity
        and innovation, it is that we discover and invent new and better ideas
        at the margins and intersections of different ways of thinking. The
        movement toward multi-, trans- and inter-disciplinarity is a sign of our
        recognition that design matters but also has the unintended consequence
        of solidifying human behaviour into structures that reflect the worst
        characteristics of human nature.
      </p>
      <p>
        If we have come to understand anything about human nature, it is that
        humans are tribal creatures whose implicit bias has been formed by the
        ability to recognize patterns and to categorize, and the instinct to
        favour the familiar and safe and to avoid difference and
        risk.
      </p>
      <p>
        Diversity has become our greatest asset but our politics, our economics,
        and our built environment favour homogeneity and separation.
      </p>
