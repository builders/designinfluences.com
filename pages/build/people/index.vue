      <p>Interdependence theory was first introduced by Harold Kelley and John Thibaut in 1959 in their book, The Social Psychology of Groups. This book drew inspiration from social exchange theory and game theory, and provided key definitions and concepts instrumental to the development of the interdependence framework. In their second book, Interpersonal Relations: A Theory of Interdependence, the theory was completely formalized in 1978. Harold Kelley continued the development of Interdependence theory in 2003, with the book An Atlas of Interpersonal Situations. This book expanded on the previous work by adding two additional dimensions to the dimensions of interdependence, as well as by analyzing 21 specific situation types. In addition, the work of Kelly and Thibaut built on the work of Kurt Lewin, who first defined interdependence, and stated that “The essence of a group is not the similarity or dissimilarity of its members, but their interdependence … A change in the state of any subpart changes the state of any other subpart … Every move of one member will, relatively speaking, deeply affect the other members, and the state of the group” (pp. 84–88).</p>
