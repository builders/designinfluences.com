      <h3>1. The Principle of Structure (The Situation)</h3>
      <p>All interactions are set within the context of their given situation (known in Interdependence theory as structure). In order to best analyze this factor, Interdependence theory presents a taxonomy of situations that includes the six dimensions listed below. A key concept with the Principle of Structure is Affordance, or what the situation affords (makes possible) for the individuals within the interaction.</p>
      <h3>2. The Principle of Transformation (What People Make of the Situation)</h3>
      <p>Transformation is a psychological process through which individuals consider possible outcomes that result from both their action and the action of others, and weigh these outcomes against possible actions and courses of actions (Rewards and costs).</p>
      <h3>3. The Principle of Interaction (SABI: I = ƒ[A, B, S])</h3>
