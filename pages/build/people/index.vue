      <h3>4. The Principle of Adaptation</h3>
      <p>Adaption refers to the process by where exposure to similar situations gives rise to habitual responses which have been proven to result in (on average) positive outcomes. In addition to the type of exposure based condition just described, adaptation can result based on rules of social norms. For example, person A might enter into a situation that is similar to situations he/she has experienced before, based on these previous experiences person A’s actions are guided in a way in which to receive the same positive outcomes as the previous situations produced. Similarly, social norms guide individuals toward specific, society approved, actions.</p>
