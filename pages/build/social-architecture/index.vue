      <p>I am making myself useful by articulating a different story about the value of design and how we can repurpose our role as designers, reinventing ourselves to be part of the movement toward deconstructing our social, economic, and political lives by decentralization of our digital infrastructure, decoupling our data from monolithic application ecosystems, and giving power back to the people by creating apps that can’t be evil.</p>
