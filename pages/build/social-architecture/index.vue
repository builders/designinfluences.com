        <h2>The Doctrine of Discovery</h2>
      </a>
      <p>So, how did we come to design the world this way?</p>
      <blockquote>
        <p>The Doctrine of Discovery established a spiritual, political, and legal justification for colonization and seizure of land not inhabited by Christians. It has been invoked since Pope Alexander VI issued the Papal Bull “Inter Caetera” in 1493. The Papal decree aimed to justify Christian European explorers’ claims on land and waterways they allegedly discovered, and promote Christian domination and superiority, and has been applied in Africa, Asia, Australia, New Zealand, and the Americas. If an explorer proclaims to have discovered the land in the name of a Christian European monarch, plants a flag in its soil, and reports his “discovery” to the European rulers and returns to occupy it, the land is now his, even if someone else was there first. Should the original occupants insist on claiming that the land is theirs, the “discoverer” can label the occupants’ way of being on the land inadequate according to European standards. This ideology supported the dehumanization of those living on the land and their dispossession, murder, and forced assimilation. The Doctrine fueled white supremacy insofar as white European settlers claimed they were instruments of divine design and possessed cultural superiority.</p>
      </blockquote>
