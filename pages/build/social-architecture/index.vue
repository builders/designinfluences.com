      <a href="#disrupting-the-status-quo" id="disrupting-the-status-quo">
        <h1>Disrupting the Status Quo</h1>
      </a>
      <p>There is a theory about how to keep organizations from dying when faced with an environment of constant technological change. The mantra is to adapt or die.</p>
      <p>The problem is that there is a familiar pattern to the way large social organizations tend to grow. At the beginning, individuals form small groups. These groups find ways to work together that are much more beneficial for the group as a whole. By specializing, each person can contribute to the work of the group.</p>
      <ul>
        <li>Belonging and identity</li>
        <li>Food and shelter</li>
        <li>Safety and security</li>
      </ul>
      <p>In the beginning, individuals form networks of people that work together to create and innovate. By working together, they discover advantages in creative collaboration that would otherwise be impossible to accomplish as individuals.</p>
      <p>Over time, there is a tendency for organizations to become hierarchies. The group becomes larger and more complex. People become managers of groups and managers of managers.</p>
      <p>John P. Kotter wrote a book called <a href="https://www.kotterinc.com/book/accelerate/" target="_blank">XLR8: Accelerate</a>. The basic idea is that networks are the creators and innovators who create value for the organization. The hierarchies serve the purpose of maintaining the organization and structure that have been found to support the work of the group.</p>
      <p>At scale, there is a tendency for the hierarchy to develop a predatory relationship to the network. Monarchs, prime ministers, presidents, and CEOs will often abuse their position to hoard influence, wealth, and power.</p>
      <p>When the leaders of the organization are more concerned about their position at the top of the hierarchy than the well-being of the networks of people that they are intended to serve, they tend to dictate the identity, capacity, and agency of the network to serve the needs of the hierarchy.</p>
      <p>We can see this pattern at the scale of companies, cities, states, nations, and multi-national corporations.</p>
