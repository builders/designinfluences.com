        <h2>The Crystal Symbol of A New Faith</h2>
      </a>
      <p>One hundred years ago, in April 1919, the Bauhaus was formed to rebuild society out of the disaster of the Great War. World War I, as we call it now, led to the defeat of the nation and the collapse of the German monarchy, and Weimar became the centre of the nation’s first experiment in democracy.</p>
      <p>The Bauhaus was also an experiment in uniting the arts and crafts into a single school to teach the next generation how to remake society. Architects and artists gathered to create and innovate the modern world.</p>
      <blockquote>
        <p>Together let us desire, conceive, and create the new structure of the future, which will embrace architecture and sculpture and painting in one unity and which will one day rise toward Heaven from the hands of a million workers like the crystal symbol of a new faith.</p>
      </blockquote>
      <p>The influence of the Bauhaus can be seen in the steel, glass, and concrete skyscrapers and buildings that define our city skylines, in the work of Dieter Rams at Braun, and in the leadership of Jony Ive in his role as Chief Design Officer of Apple. The sans serif typography, the minimalist aesthetic, the choice of materials, and the focus on the features and user experience are all based on the fundamental principles of design that were originally formulated into a design curriculum.</p>
      <p>For those who are familiar with Designlab, the Bauhaus was the original laboratory of modern design.</p>
      <p>Our aluminum, glass, and silicon devices are the most recent manifestation of the Bauhaus influence.</p>
