      </a>
      <p>Part of our own digital transformation is in our understanding about how design is evolving. From the physical artifact to living systems.</p>
      <p>I was having a discussion about design thinking with Johanna Desbordes, Program Manager, Global Host Community at Airbnb. We shared the same concerns about our obsession with design sprints for small manageable problems that we feel empowered to address. However, in the process, we lose sight of the big picture and the much larger problems that we need to solve.</p>
