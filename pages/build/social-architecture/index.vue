        <h2>What is the builders collective?</h2>
      </a>
      <p>The builders collective is what I would call the Bauhaus, if I was to translate it into English. I go by the name bauhouse on most online platforms. But I keep on having to spell it out for people. So, I wanted to find something that might work a little easier.</p>
      <p>For me, the builders collective is the creative, collaborative network of people who are creating and innovating a future based on sustainability and symbiosis with the ecological ecosystem that humans inhabit.</p>
